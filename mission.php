<h1>Mission</h1>
This website has been established to inform the public, especially those concerned with the traditions of scouting about the rich history and the current and future needs of Camp Merz, Boy Scout Camp, Mayville New York.<br>
<br>

This website is to serve as a portal of information for maintaining the original intent and purpose of Camp Merz and Frank Merz's generous donation.<br>
<br>

<h2>FACT #1:</h2>
When the deed was transferred to the Boy Scout Council in 1933 that created Camp Merz, an agreement was signed by all involved and recorded as part of the transaction that stated that any funds produced by the sale of any land sale would be deposited in a trust account titled the Frank Merz Trust and the income then produced would be used to support the activities of the camp.<br>
<br>

<h2>FACT #2:</h2>
Four parcels (perhaps more) have been sold.
<ul>
  <li>Parcel #1 was lake frontage to a neighbor.</li>
  <li>Parcel #2 land was sold to the state for storm water control.</li>
  <li>Parcel #3 home site at rear of property.</li>
  <li>Parcel #4 home site at rear of property.</li>
</ul>
<br>

<h2>FACT #3:</h2>
All of the above is on record in the land records at Mayville.<br>
