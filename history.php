<h1>History</h1>
<h2>Camp Merz</h2>
The original parcel of land consists of about 360 acres and was purchased in 1933 at a tax sale. The funds required to buy the property from Chautauqua County were provided by the well known Jamestown banker Frank Merz.<br>
<br>

Located on Route 394 outside Mayville, NY. the land was donated in 1933 and opened for Boy Scout camping as Camp Merz in 1935.  In the early years scouts pitched tents and had rudimentary mess and latrine facilities. As the camp developed over the years many structures were erected to accommodate the scouts and enhance the camping experience.<br>
<br>

Today's Camp Merz consists of several sleeping cabins, showers, water front recreation center, swimming and boating pier, meeting hall and administration building as well as a rifle range and trading post. The original Ranger residence is still occupied and part of the grounds.<br>
<br>

Today Scouts enjoy camping, boating, shooting, team games, camp fires, and many other activities that build character and self reliance in the Scouting tradition.<br>
<br>
<br>

<h2>Frank Merz Story</h2>
<img src="images/FrankMerz.gif" alt="Frank Merz" width="289" height="139"><br>
Merz was motivated to do something for young boys by his own life experiences and personal hardships. The experience of being orphaned and his four sons dying in their infancy caused Frank Merz to truly want to make a difference In the lives of young boys.<br>
<br>

Frank Merz, <em>President of the Union Trust Company at the time of his gift in 1933</em>, was born in July 1857, the youngest of six children. Although Mr. Merz reached the pannacle of professional and social standing in the community his beginnings were beset by hardship.<br>
<br>

His parents had emigrated from Germany in 1847, but his mother died in 1859 and his father died just two years later, in 1861, leaving all to be raised by relatives in Ontario.<br>
<br>

Returning to the States, at ten years of age, Frank and his younger siblings lived with their eldest brother Benjamin. When hardship came again to the family he was forced to board and work for an elderly widow....<br>
