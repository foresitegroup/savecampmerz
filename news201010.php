<h1>October 2010</h1>
It is encouraging to be able to report that there have been some major improvements to Camp Frank Merz in recent times.<br>
<br>

First, a new water distribution has been installed underground. Water is now available at every site and venue throughout the camp, even at the rifle range. Areas are served by farm type freeze proof hydrants.<br>
<br>

Second, a new large pavilion has been built at almost every camping site. A similar pavilion was also built at the rifle range where it was sorely needed.<br>
<br>

Third, a new large bathhouse has been remodeled and enlarged in a central location within the camp. This is for the exclusive use of Scouts.<br>
<br>

Four, the Dawson building received a new roof in 2009 thanks to a grant from NY State. The Dawson Building is now served by solar panels for electricity.<br>
<br>

While these improvements are noteworthy it is disappointing that the council continues to state that no property has been sold, when in fact two residential lots have been sold, one parcel of lake frontage has been sold and a small parcel was sold to the State for storm sewer construction. These four transactions all produced about $100,000 CASH for the sale of the land of Camp Merz! It is disappointing that the council does not seem to see fit to live up to the terms of the deed for the property created in 1933 wherein it is required and was agreed that funds received from all such land sales be deposit in a trust account to be created and named the Frank Merz Trust for the future benefit of Camp Merz and Boy Scouts. While it might be argued that the money from the sales was used for the Boy Scouts, that is not the same as establishing a trust in the name of Frank Merz as was intended by those involved at the time the deed was passed. Failure to live up to an agreement is not a good lesson for young boys.<br>
<br>

It is further discouraging to note that the Council continues to support the absolutely totally false story about <!--<a href="http://www.campmerz.org/2008/DIamondStory/Legend6.html" target="_new">-->Frank Merz's trip to Mayville and his finding a temple containing a diamond<!--</a>-->.<br>
